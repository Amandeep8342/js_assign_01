
let array1=[]; // new array
array1.push(15);// adding value in last of array and now array is [15]
console.log(array1);
array1.unshift(5);// adding value starting of array and now array is [5,15]
console.log(array1);
array1.push(20); // adding one more values to end of array now array is [5,15,20]
array1.pop();// deleting last item from array now array is [5,15]
array1.shift();// removing first value now array is [15]
console.log(array1);

